# Identificadores devem começar com _ ou a-z, A-Z.
# Identificadores não podem começar por números. 
# Identificadores não podem ser palavras reservadas pelo Python.

# Convenções:
# _my_var indica que essa é um objeto de uso interno ou de uso privado. 
# Variáveis ou funções com '_' na frente não serão importadas pelo 
# interpretador. Por exemplo:
'''
from module import * 
'''
# Nesse caso, funções ou classes que comecem por '_' não serão importadas. 



'''
Identificadores do tipo __my_var__
'''
# Eles são reservados pelo interpretador do Python. Eles são nomes reservadors
# pelo sistema. 


